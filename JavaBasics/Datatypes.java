package JavaBasics;

public class Datatypes {
    public static void main(String args[]) {

        // In primitive Data types they are 8 data types

        byte b = 8; // Byte data type
        System.out.println(b);

        short n = 240; // Short data type
        System.out.println(n);

        char ch = 'a'; // Character data type
        System.out.println(ch);

        boolean var = true; // Boolean only store True or False
        System.out.println(var);

        int a = 10; // Integer data type
        System.out.println(a);

        long big = 1000; // Long data type
        System.out.println(big);

        float decimal = 2.3f; // Float data type
        System.out.println(decimal);
        
        double tag = 10.5; // Double data type
        System.out.println(tag);

    }
}
