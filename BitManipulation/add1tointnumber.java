package BitManipulation;

public class add1tointnumber {
    public static void main(String[] args) {

        // using Bitwise NOT Operator

        int x = 6;
        System.out.println(x + " + " + 1 + " is " + -~x);

        x = -4;
        System.out.println(x + " + " + 1 + " is " + -~x);

        x = 0;
        System.out.println(x + " + " + 1 + " is " + -~x);

    }
}
